// Assignment Operator

/*

| Operator | Example  | Same as     |
| -------- | -------  | -------     |
|    =     |  x = 5   |  x = 5      | 
|    +=    |  x += 5  |  x = x + 5  |
|    -=    |  x -= 5  |  x = x - 5  |
|    *=    |  x *= 5  |  x = x * 5  |
|    /=    |  x /= 5  |  x = x / 5  |
|    %=    |  x %= 5  |  x = x % 5  |
|    **=   |  x **= 5 |  x = x ** 5 |

*/

let box = 10
box += 5
console.log(box);
box -= 5
console.log(box);
box *= 5
console.log(box);
box /= 5
console.log(box);
box %= 5
console.log(box);
box **= 5
console.log(box);
