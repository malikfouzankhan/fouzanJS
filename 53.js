// JavaScript and JSON
/*
What is JSON?
JSON stands for JavaScript Object Notation. It's a lightweight format for storing and tansferring data between a client and a server.

JSON (JavaScript Object Notation) is the universal language of data interchange in modern computing. It's lightweight, human-readable, and machine-parseable. Every major programming language (Python, Java, Go, Rust, etc.) can parse and generate JSON easily.

Universal format (used in 99% APIs)

No need for extra encoding/decoding layers

It looks like JavaScript objects but is actually a text format.
*/

/*
Why JSON is universal

Property                        Explanation
----------------------------------------------------------------------------------
Language independent            Works in all languages (JS, Python, Go, etc.)
Lightweight                     Faster than XML or YAML
Human + Machine-readable        Easy for both humans and programs
Flexible                        Supports nested structures, arrays, objects
Standardized                    Defined by ECMA-404
Secure                          No code execution (unlike XML parsers)
Extensible                      Can be used for configs, APIs, logs, data storage
*/